    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres(), cliDB.getProfesion());
}

    public void graficar(int h, int m, String profecion)
    {
        try
        {
           DefaultCategoryDataset ds=new DefaultCategoryDataset();
           float total=(float)(h+m);
           float porH=((h*100)/total);
           float porM=((m*100)/total);
           ds.addValue(porH, "Hombres", "Hombres");
           ds.addValue(porM, "Mujeres", "Mujeres");
           
            JFreeChart jf= ChartFactory.createBarChart3D("Profecion: "+profecion, "Genero", "Cantidad(%)", ds, PlotOrientation.VERTICAL, 
                    true, true, true);
            
            ChartFrame f = new ChartFrame("Estadistica por sexo", jf);
            f.setSize(400, 400);
            //f.setLocationRelativeTo(LEFT_ALIGNMENT);
            f.setLocation(900, 200);
            f.setVisible(true);
        }catch( Exception e)
        {
            System.out.println("Eroor"+e);
        }
    }
